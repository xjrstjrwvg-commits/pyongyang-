def get_clean_char(w, ignore_voiced, pos="head"):
    if not w: return ""
    # 長音を除去
    w_clean = w.replace("ー", "")
    if not w_clean: return ""
    char = w_clean[0] if pos == "head" else w_clean[-1]
    # 小文字を大文字に
    res = SMALL_TO_LARGE.get(char, char)
    # 濁点無視設定なら清音化
    if ignore_voiced:
        res = res.translate(VOICE_MAP)
    return res

